#include "libft.h"

void	ft_putnbr(int n)
{
	int nb;
	int c;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	nb = n;
	if (n < 0)
	{
		ft_putchar('-');
		nb = -n;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + 48);
}
